#include "libft.h"

int	ft_isxdigit(int c)
{
	unsigned char	x;

	x = (unsigned char)c;
	if (ft_isdigit(c) || ('A' <= x && x <= 'F') || ('a' <= x && x <= 'f'))
		return (1);
	return (0);
}
